var nome1 = "josé"
var nome2 = "maria"

if nome1 < nome2 {
    print("Ordenação dos nomes: \(nome1) -> \(nome2)")
}

// Será impresso "Ordenação dos nomes: josé -> maria"
// Perceba que utilizamos o operador < para descobrir se alfabeticamente
// nome1 vem antes de nome2.
